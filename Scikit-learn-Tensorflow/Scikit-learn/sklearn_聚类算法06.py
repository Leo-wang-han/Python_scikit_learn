# 聚类算法简介
"""
聚类方法是最有用的无监督Ml方法之一。
这些方法用于查找数据样本之间的相似性以及关系模式，然后将这些样本基于特征聚类为具有相似性的组
聚类很重要，因为它决定了未标记数据之间的固有分组。
他们基本上对数据点进行一些假设以构成他们的相似性。每个假设都会构建不同但有效的聚类
"""

# 簇的形成方法
"""
1.基于密度:噪声应用空间聚类（DBSCAN），识别聚类结构的排序点(OPTICS)
2.基于等级：群集根据层次结构形成为树形结构。凝聚(自下而上的方法)和分裂（自上而下的方法）:
使用代表（CURE）进行聚类，使用层次结构（BIRCH）进行平衡的迭代式减少聚类
3.分区：通过将对象分成k个簇来形成簇。群集数等于分区数：k均值，基于随机搜索（CLARANS)对大型应用程序进行聚类
4.网格：簇形成网格状结构。优点是在这些网格上完成的所有聚类操作都是快速的，并且与数据对象的数量
无关。：统计信息网格（STING),任务重的聚类（CLIQUE）
"""

