# 剪影分析
"""
剪影分析用于通过测量聚类之间的距离来检查聚类模型的质量。
他基本上为我们提供了一种借助剪影得分评估簇数等参数的方法。
此分数衡量一个集群中的每个点与相邻群集中的点的接近程度

剪影得分的范围是[-1，1]。其分析如下-
+1分数-接近+1 剪影分数表示样本距离其邻近簇很远。
0得分-0 剪影得分表示样本在将两个相邻聚类分开的决策边界上或非常接近。
-1分数-1 剪影分数表示样本已分配给错误的聚类。
使用下面的公式可以计算出轮廓分数
$silhouettescore= :(p−q）/max（p，q）
在这里，p =到最近簇的点的平均距离
而且，q =到所有点的平均集群内距离。
"""

